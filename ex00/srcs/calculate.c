int	calculate(int a, int b, char c)
{
	if (c == '/')
		return (a / b);
	if (c == '+')
		return (a + b);
	if (c == '-')
		return (a - b);
	if (c == '%')
		return (a % b);
	if (c == '*')
		return (a * b);
}
